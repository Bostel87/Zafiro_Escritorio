    /**
     * Esta función permite consultar los registros de acuerdo al criterio seleccionado.
     * @return true: Si se pudo consultar los registros.
     * <BR>false: En el caso contrario.
     */
    private boolean consultarReg(){
        boolean blnRes=true;
        try{
            conCab=DriverManager.getConnection(objParSis.getStringConexion(), objParSis.getUsuarioBaseDatos(), objParSis.getClaveBaseDatos());
            if (conCab!=null){
                stmCab=conCab.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
                //Validar que sólo se muestre los documentos asignados al programa.
                strSQL="";
                strSQL+="SELECT a1.co_emp, a1.co_grp";
                strSQL+=" FROM tbm_grpClaModInv AS a1 LEFT OUTER JOIN tbm_claModInv AS a2";
                strSQL+=" ON a1.co_emp=a2.co_emp AND a1.co_claModInv=a2.co_claModInv";
                strSQL+=" WHERE a1.co_emp=" + objParSis.getCodigoEmpresa() + "";
                strSQL+=" AND a1.st_reg='A' ";

                strAux=txtCodGrp.getText();
                if (!strAux.equals(""))
                    strSQL+=" AND a1.co_grp=" + strAux.replaceAll("'", "''") + "";

                strAux=txtDesCorGrp.getText();
                if (!strAux.equals(""))
                    strSQL+=" AND LOWER(a1.tx_desCor) LIKE '" + strAux.replaceAll("'", "''").replace('*', '%').toLowerCase() + "' ";

                strAux=txtDesLarGrp.getText();
                if (!strAux.equals(""))
                    strSQL+=" AND LOWER(a1.tx_desLar) LIKE '" + strAux.replaceAll("'", "''").replace('*', '%').toLowerCase() + "' ";
                
                strAux=txtCodGrpPad.getText();
                if (!strAux.equals(""))
                    strSQL+=" AND a1.co_grpPad=" + strAux.replaceAll("'", "''") + "";
                
                strAux=txtCodCla.getText();
                if (!strAux.equals(""))
                    strSQL+=" AND a1.co_claModInv=" + strAux.replaceAll("'", "''") + "";
                
                strSQL+=" ORDER BY a1.co_grp";
                System.out.println("consultarReg: " + strSQL);
                rstCab=stmCab.executeQuery(strSQL);
                if (rstCab.next()){
                    rstCab.last();
                    objTooBar.setMenSis("Se encontraron " + rstCab.getRow() + " registros");
                    rstCab.first();
                    cargarReg();
                }
                else
                {
                    mostrarMsgInf("No se ha encontrado ningún registro que cumpla el criterio de búsqueda especificado.");
                    limpiarFrm();
                    objTooBar.setEstado('l');
                    objTooBar.setMenSis("Listo");
                }
            }
        }
        catch (java.sql.SQLException e)
        {
            blnRes=false;
            objUti.mostrarMsgErr_F1(this, e);
        }
        catch (Exception e)
        {
            blnRes=false;
            objUti.mostrarMsgErr_F1(this, e);
        }
        return blnRes;
    }
    

