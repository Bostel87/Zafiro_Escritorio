            txtDesLarCla.setBackground(objParSis.getColorCamposObligatorios());


            objTooBar=new MiToolBar(this);
            panBar.add(objTooBar);

            getTipoGrupoUsuario();

            //Configurar los JTables.
            configurarTblDat();
            //configurarVenConCla();
            //configurarVenConGrp();
            txtCodGrp.setVisible(false);
            txtCodGrp.setEditable(false);
            txtCodGrp.setEnabled(false);

            txtCodCla.setVisible(false);
