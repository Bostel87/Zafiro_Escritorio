    /** Configurar el formulario. */
    private boolean configurarFrm()
    {
        boolean blnRes=true;
        try
        {
            
            
            //Configurar JTable: Establecer el modelo.
            Vector vecDat=new Vector();    //Almacena los datos
            Vector vecCab=new Vector();    //Almacena las cabeceras
            vecCab.clear();
            vecCab.add(INT_TBL_LINEA,"");            
            vecCab.add(INT_TBL_ITMALT,"Cod. Item");
            vecCab.add(INT_TBL_DESITM,"Descripcion");            
            vecCab.add(INT_TBL_UNIDAD,"Unidad");
            vecCab.add(INT_TBL_CANMOV,"Cantidad");
           
            objTblMod=new Librerias.ZafTblUti.ZafTblMod.ZafTblMod();
            objTblMod.setHeader(vecCab);
            tblDat.setModel(objTblMod);   
            
            tblDat.setRowSelectionAllowed(true);
            tblDat.setSelectionMode(javax.swing.ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
            objColNum=new Librerias.ZafColNumerada.ZafColNumerada(tblDat,INT_TBL_LINEA);
           
            javax.swing.table.TableColumnModel tcmAux=tblDat.getColumnModel();
             
              //Tamaño de las celdas
            tcmAux.getColumn(INT_TBL_LINEA).setPreferredWidth(30);         
            tcmAux.getColumn(INT_TBL_ITMALT).setPreferredWidth(100);
            tcmAux.getColumn(INT_TBL_DESITM).setPreferredWidth(200);
            tcmAux.getColumn(INT_TBL_UNIDAD).setPreferredWidth(70);
            tcmAux.getColumn(INT_TBL_CANMOV).setPreferredWidth(70);
               
            objTblCelRenLbl=new Librerias.ZafTblUti.ZafTblCelRenLbl.ZafTblCelRenLbl();
            objTblCelRenLbl.setHorizontalAlignment(javax.swing.JLabel.RIGHT);
            objTblCelRenLbl.setTipoFormato(objTblCelRenLbl.INT_FOR_NUM);
            objTblCelRenLbl.setFormatoNumerico(objParSis.getFormatoNumero(),true,true);
            tcmAux.getColumn(INT_TBL_CANMOV).setCellRenderer(objTblCelRenLbl);
            
            tblDat.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
            tblDat.getTableHeader().setReorderingAllowed(false);      
            
            tcmAux=null;  
            strAux=objParSis.getNombreMenu();
            this.setTitle(strAux + " v0.2");    
            lblTit.setText(strAux);
           // arlDat=new ArrayList();  
        }
        catch(Exception e)
        {
            blnRes=false;
            objUti.mostrarMsgErr_F1(this, e);  
        }
        return blnRes;
    }

  
