    private class ZafThreadGUI extends Thread
    {
        public void run()
        {  
             boolean blnRes=false;
             butCon.setText("Detener");
             lblMsgSis.setText("Procesando datos...");
              pgrSis.setIndeterminate(true);
         
                if (cargarRegistro()){
                  
                   blnRes=true;
                 }
              
                if(blnRes==true){
                     lblMsgSis.setText("Listo");
                     pgrSis.setIndeterminate(false);
                     pgrSis.setValue(0);
                     butCon.setText("Consultar");
                 
                     
                }
                else{
                     lblMsgSis.setText("Error..");
                     pgrSis.setIndeterminate(false);
                     pgrSis.setValue(0);
                     butCon.setText("Consultar");
                }
 
 
            objThrGUI=null;
    }}
    
             
    
      
      
    private boolean cargarRegistro(){
        boolean blnRes=false;
        String sql="";
        try{
            
               con=DriverManager.getConnection(objParSis.getStringConexion(), objParSis.getUsuarioBaseDatos(), objParSis.getClaveBaseDatos() );
               if (con!=null)
               {
                sql="  SELECT * FROM (  " +
                "  select c.co_itm, c.tx_codalt, c.tx_nomitm , sum(nd_can) as CanMov , var.tx_descor from tbm_cabmovinv as a " +
                "  inner join tbm_detmovinv as b on (b.co_Emp=a.co_emp and b.co_loc=a.co_loc and b.co_tipdoc=a.co_tipdoc and b.co_doc=a.co_doc) " +
                "  inner join tbm_inv as c on(c.co_emp=b.co_emp and c.co_itm=b.co_itm and c.st_ser='S')   " +
                "  left join  tbm_var as var on (var.co_reg=c.co_uni) " +
                "  where a.co_emp="+objParSis.getCodigoEmpresa()+"   and a.st_reg not in ('I','E')  " +  
                "  group by c.co_itm, c.tx_codalt  , c.tx_nomitm , var.tx_descor  order by c.tx_codalt" + 
                " ) as  x where CanMov<0";
                java.sql.Statement stm = con.createStatement();
                java.sql.ResultSet rst = stm.executeQuery(sql);
                Vector vecData = new Vector();
                while(rst.next()){
                       java.util.Vector vecReg = new java.util.Vector();
                       vecReg.add(INT_TBL_LINEA, "");
                       vecReg.add(INT_TBL_ITMALT, rst.getString("tx_codalt"));
                       vecReg.add(INT_TBL_DESITM, rst.getString("tx_nomitm"));
                       vecReg.add(INT_TBL_UNIDAD, rst.getString("tx_descor"));
                       vecReg.add(INT_TBL_CANMOV, new Double(rst.getDouble("CanMov")));
                       vecData.add(vecReg);   
                 }
                 objTblMod.setData(vecData);
                 tblDat .setModel(objTblMod);  
                     
               rst.close();
               rst=null;
               stm.close();
               stm=null;
               blnRes=true;
        }}
        catch (java.sql.SQLException e) { objUti.mostrarMsgErr_F1(this, e);   }
        catch (Exception e) {  objUti.mostrarMsgErr_F1(this, e);   }
       return blnRes;
    }
  
   
   
   
   
        
    
    
   
   
    
    
 
    
