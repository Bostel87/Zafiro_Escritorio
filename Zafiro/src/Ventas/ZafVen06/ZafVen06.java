                    objVenCon.show();
                    if (objVenCon.acepto())
                    {
                        txtCodItm.setText(objVenCon.GetCamSel(1));
                        txtCodAlt.setText(objVenCon.GetCamSel(2));
                        txtNomItm.setText(objVenCon.GetCamSel(3));
                    }
